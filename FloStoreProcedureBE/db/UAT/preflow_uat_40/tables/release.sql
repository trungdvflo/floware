CREATE TABLE `release` (
  `id` bigint(20) unsigned NOT NULL AUTO_INCREMENT,
  `user_id` bigint(20) unsigned NOT NULL COMMENT 'Just tracking who upload/create this release Flo app',
  `version` varchar(25) CHARACTER SET utf8 NOT NULL DEFAULT '' COMMENT 'Version Flo app released',
  `checksum` varchar(200) CHARACTER SET utf8 NOT NULL DEFAULT '' COMMENT 'It is string and it will be hashed by client side',
  `release_note` text COMMENT 'Admin can note anything on this field',
  `description` text CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci,
  `file_name` varchar(200) NOT NULL COMMENT 'This is real name of file which uploaded to server side (it will be shown on the UI of Admin page)',
  `file_uid` varchar(100) DEFAULT NULL COMMENT 'It is ID of file which stored in HDD (it will be generated by server side)',
  `app_id` varchar(255) NOT NULL COMMENT 'Flo app type, default =0 : flo mac app (refer to table app_register)',
  `build_number` decimal(16,0) NOT NULL COMMENT 'Unique number that marks the application build number.',
  `title` varchar(128) NOT NULL DEFAULT '',
  `message` varchar(512) NOT NULL DEFAULT '',
  `expire_date` double(13,3) DEFAULT '0.000' COMMENT 'Time expire of this build',
  `message_expire` varchar(5000) NOT NULL DEFAULT '',
  `os_support` varchar(45) DEFAULT NULL COMMENT 'Minimum operating system supported for installation',
  `length` int(11) NOT NULL COMMENT 'The size of the zip file is uploaded',
  `file_dsym` varchar(100) NOT NULL COMMENT 'Key check reports application crashes',
  `file_dsym_uid` varchar(100) DEFAULT NULL,
  `length_dsym` int(11) NOT NULL COMMENT 'The size of the dsym file is uploaded',
  `url_download` varchar(512) DEFAULT NULL,
  `release_type` enum('0','1') NOT NULL DEFAULT '0' COMMENT 'Value: \n\n  * 0: Minor: normal update\n  * 1: Major: any build that requires a removal of existing data and reinstall.',
  `release_time` double(13,3) DEFAULT NULL,
  `release_status` enum('0','1','2','3') DEFAULT NULL,
  `upload_status` tinyint(1) DEFAULT NULL,
  `created_date` double(13,3) NOT NULL,
  `updated_date` double(13,3) DEFAULT NULL,
  PRIMARY KEY (`id`) USING BTREE,
  UNIQUE KEY `file_dsym_uid_UNIQUE` (`file_dsym_uid`),
  UNIQUE KEY `file_uid_UNIQUE` (`file_uid`),
  KEY `file_name` (`file_name`) USING BTREE,
  KEY `checksum` (`checksum`) USING BTREE,
  KEY `os_support` (`os_support`) USING BTREE,
  KEY `release_type` (`release_type`) USING BTREE,
  KEY `release_time` (`release_time`) USING BTREE,
  KEY `release_status` (`release_status`) USING BTREE,
  KEY `created_date` (`created_date`) USING BTREE,
  KEY `app_id` (`app_id`) USING BTREE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 ROW_FORMAT=DYNAMIC